celcius = int(input("Masukkan suhu dalam Celcius: "))


kelvin = celcius + 273.15
print("Hasil konversi dari suhu", celcius, "derajat Celcius ke Kelvin adalah:", kelvin, "K")


reamur = (4/5) * celcius
print("Hasil konversi dari suhu", celcius, "derajat Celcius ke Reamur adalah:", reamur, "R")


fahrenheit = ((9/5) * celcius) + 32
print("Hasil konversi dari suhu", celcius, "derajat Celcius ke Fahrenheit adalah:", fahrenheit, "F")
